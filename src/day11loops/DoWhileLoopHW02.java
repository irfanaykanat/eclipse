package day11loops;

import java.util.Scanner;

public class DoWhileLoopHW02 {

	public static void main(String[] args) {
		/*
		Write a program to print numbers from 1 to 5 on the console by using do-while loop.
		
		
		 */

	
		
		int counter =0;
		
		do {
			
			System.out.println(counter+1);
			counter++;
			
		}while (counter<5);
		
	}

}
