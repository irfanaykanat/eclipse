package day11loops;

import java.util.Scanner;

public class DoWhileLoopHW01 {

	public static void main(String[] args) {
		/*
         Task:  Write a program to print numbers from 10 to 3 on the console by using do-while loop.		
		
		 */

	
		
		int counter =11;
		
		do {
			
			System.out.println(counter-1);
			counter--;
			
		}while (counter>3);
		
	}

}
